//以(cx,cy)为左上角画字母,letter[5x5]
LetterFactory.prototype.drawLetter = function(brush, letterIndex, colorIndex, cx, cy){
	var row, col, value;
	for (row = 0; row < 5; row++){
		for (col = 0; col < 5; col++){
			value = this.letters[letterIndex][row * 5 + col];
			if (value != 0){
				value = colorIndex;
			}
			brush.draw(row + cy, col + cx, value);
		}
	}
};
//显示字母
LetterFactory.prototype.showLetter = function(brush, index){
	if (index >=0 && index < 20){
		var word = this.words[index];
		var colorIndex = 1 + Math.round(Math.random() * 3);
		this.drawLetter(brush, word[0], colorIndex, word[1], word[2]);
	}
};
